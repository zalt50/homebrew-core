class Qcachegrind < Formula
  desc "Visualize data generated by Cachegrind and Calltree"
  homepage "https://apps.kde.org/kcachegrind/"
  url "https://download.kde.org/stable/release-service/26.04.3/src/kcachegrind-26.04.3.tar.xz"
  sha256 "5a071482af4c999081eca0da798c457db33cfc791c133859e8c9877cebd22577"
  license "GPL-2.0-or-later"
  head "https://invent.kde.org/sdk/kcachegrind.git", branch: "master"

  # We don't match versions like 19.07.80 or 19.07.90 where the patch number
  # is 80+ (beta) or 90+ (RC), as these aren't stable releases.
  livecheck do
    url "https://download.kde.org/stable/release-service/"
    regex(%r{href=.*?v?(\d+\.\d+\.(?:(?![89]\d)\d+)(?:\.\d+)*)/?["' >]}i)
  end

  bottle do
    sha256 cellar: :any, arm64_tahoe:   "3634311927cecd137d93117514072acd56ebf1530ac40bfc58bf6e8ea9fb012f"
    sha256 cellar: :any, arm64_sequoia: "f452c5e0e3d17ac4973794428338a1dd2e52069d7c5ce305acf22e5fb16e6c1b"
    sha256 cellar: :any, arm64_sonoma:  "788ee17ce750ecb9a589b17f37ece4128fe8eba36daf8a3032c8d3feb85683a1"
    sha256 cellar: :any, sonoma:        "527cab82eb667357400b275a4c5fafbce13de3342fb4138707a25de38bc57ed1"
    sha256 cellar: :any, arm64_linux:   "cf6a967617605f2b4e46008316986348999788090a77c6a9bbb8bd0280af1793"
    sha256 cellar: :any, x86_64_linux:  "31b97bb0992776b2403a788ec1a9e428a49edef9e431e001f02256c570420246"
  end

  depends_on "graphviz"
  depends_on "qtbase"

  def install
    args = %w[-config release]
    if OS.mac?
      spec = (ENV.compiler == :clang) ? "macx-clang" : "macx-g++"
      args += %W[-spec #{spec}]
    end

    qt = Formula["qtbase"]
    system qt.opt_bin/"qmake", *args
    system "make"

    if OS.mac?
      prefix.install "qcachegrind/qcachegrind.app"
      bin.install_symlink prefix/"qcachegrind.app/Contents/MacOS/qcachegrind"
    else
      bin.install "qcachegrind/qcachegrind"
    end
  end
end
